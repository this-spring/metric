def interpolate_monthly(keypoints: list[tuple[str, float]]) -> list[dict]:
    """
    Given keypoints as [(YYYY-MM, value), ...], interpolate monthly values
    with small random noise for realism.
    """
    from datetime import date

    # Parse keypoints
    parsed = []
    for ym, val in keypoints:
        parts = ym.split("-")
        y, m = int(parts[0]), int(parts[1])
        parsed.append((y, m, val))

    result = []
    for i in range(len(parsed) - 1):
        y1, m1, v1 = parsed[i]
        y2, m2, v2 = parsed[i + 1]

        months1 = y1 * 12 + m1
        months2 = y2 * 12 + m2
        span = months2 - months1

        for j in range(span):
            t = j / max(span, 1)
            # Smooth interpolation
            val = v1 + (v2 - v1) * (3 * t * t - 2 * t * t * t)
            # Add small noise (±2%)
            noise = 1 + random.gauss(0, 0.01)
            val = round(val * noise, 2)

            cur_months = months1 + j
            cy = cur_months // 12
            cm = cur_months % 12
            if cm == 0:
                cm = 12
                cy -= 1
            d = date(cy, cm, 1)
            result.append({"date": d.strftime("%Y-%m-%d"), "value": val})

    # Add the last keypoint
    y, m, v = parsed[-1]
    d = date(y, m, 1)
    result.append({"date": d.strftime("%Y-%m-%d"), "value": round(v, 2)})

    return result


